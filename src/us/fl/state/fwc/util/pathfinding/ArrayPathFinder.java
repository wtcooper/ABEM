	private ArrayMap map; 
	/** The path finder we'll use to search our map */
	private PathFinder finder;
	/** The last path found for the current unit */
	private Path path;
	
	private int startX, startY, endX, endY;


	public ArrayPathFinder(){
	}
	
	
	public ArrayPathFinder(int startX, int startY, int endX, int endY){
		this.startX = startX;
		this.startY = startY;
		this.endX = endX;
		this.endY = endY;
		
	}
	
	public ArrayList<Int2D> compute(int[][] array, Int2D startNode, Int2D endNode){
		this.startX = startNode.x;
		this.startY = startNode.y;
		this.endX = endNode.x;
		this.endY = endNode.y;
		
		map = new ArrayMap();
		map.setTerrain(array);

		finder = new AStarPathFinder(map, 200, false, new ManhattanHeuristic(1));

		path = finder.findPath(new ArrayMover(), startX, startY, endX, endY);
		//if not path, then returns a null path, so return null
		if (path == null) return null;
		
		return path.getSteps();
	
	}
	

	
